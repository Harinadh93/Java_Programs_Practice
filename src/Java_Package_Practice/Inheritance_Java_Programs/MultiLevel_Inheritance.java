package Java_Package_Practice.Inheritance_Java_Programs;

public class MultiLevel_Inheritance {

    public void method1()
    {
        int a = 20;
        int b = 30;
        int c = a + b;
        System.out.println(c);
    }

    public void method2()
    {
        int d = 30;
        int e = 10;
        int f = d - e;
        System.out.println(f);
    }
}
